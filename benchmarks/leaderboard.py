"""
RAG Stack Benchmark — compare retrieval strategies on CRM FAQ dataset.

Runs a configurable evaluation across different retrieval configurations
and produces a leaderboard table. This is the most shareable feature
of crm-rag-eval — the results can be posted as a blog post or README badge.

Usage::

    from crm_rag_eval.benchmarks.leaderboard import run_benchmark

    results = run_benchmark(strategies={
        "BM25": my_bm25_retriever,
        "Dense (OpenAI)": my_dense_retriever,
        "Hybrid": my_hybrid_retriever,
    })
    results.print_leaderboard()
    results.to_markdown("BENCHMARK.md")
"""

from __future__ import annotations

import time
from dataclasses import dataclass, field
from typing import Any, Callable, Dict, List, Optional

from ..datasets.faq import load_faq_dataset, FAQItem
from ..evaluator import CRMRagEvaluator
from ..metrics.engine import RAGSample


@dataclass
class StrategyResult:
    name: str
    avg_groundedness: float
    avg_hallucination_risk: float
    avg_context_precision: float
    avg_context_recall: float
    avg_source_coverage: float
    avg_overall_score: float
    avg_latency_ms: float
    critical_risk_count: int
    high_risk_count: int
    n_samples: int


@dataclass
class BenchmarkReport:
    """Results of a multi-strategy benchmark run."""

    strategy_results: List[StrategyResult]
    dataset_size: int
    domains: List[str]

    def print_leaderboard(self) -> None:
        """Print a formatted leaderboard to stdout."""
        print(f"\n{'='*90}")
        print(f"  crm-rag-eval BENCHMARK LEADERBOARD  ({self.dataset_size} samples, domains: {self.domains})")
        print(f"{'='*90}")
        header = f"{'Strategy':<28} {'Overall':>8} {'Ground':>8} {'Halluc↓':>8} {'CtxPrec':>8} {'SrcCov':>8} {'Lat(ms)':>9} {'CRIT':>5} {'HIGH':>5}"
        print(header)
        print("-" * 90)
        sorted_results = sorted(self.strategy_results, key=lambda r: r.avg_overall_score, reverse=True)
        for i, r in enumerate(sorted_results):
            rank = "🥇" if i == 0 else "🥈" if i == 1 else "🥉" if i == 2 else f"#{i+1} "
            print(
                f"{rank} {r.name:<25} {r.avg_overall_score:>8.3f} {r.avg_groundedness:>8.3f} "
                f"{r.avg_hallucination_risk:>8.3f} {r.avg_context_precision:>8.3f} "
                f"{r.avg_source_coverage:>8.3f} {r.avg_latency_ms:>9.0f} "
                f"{r.critical_risk_count:>5} {r.high_risk_count:>5}"
            )
        print("="*90)
        print("Higher overall/groundedness/context_precision/source_coverage = better")
        print("Lower hallucination_risk/latency/CRIT/HIGH = better\n")

    def to_markdown(self, output_path: str) -> None:
        """Write the leaderboard as a Markdown table."""
        sorted_results = sorted(self.strategy_results, key=lambda r: r.avg_overall_score, reverse=True)
        lines = [
            f"# crm-rag-eval Benchmark Leaderboard",
            f"",
            f"Dataset: {self.dataset_size} CRM FAQ samples | Domains: {', '.join(self.domains)}",
            f"",
            f"| Rank | Strategy | Overall ↑ | Groundedness ↑ | Halluc. Risk ↓ | Ctx Precision ↑ | Src Coverage ↑ | Latency (ms) ↓ | CRITICAL | HIGH |",
            f"| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
        ]
        medals = ["🥇", "🥈", "🥉"]
        for i, r in enumerate(sorted_results):
            rank = medals[i] if i < 3 else f"#{i+1}"
            lines.append(
                f"| {rank} | **{r.name}** | {r.avg_overall_score:.3f} | {r.avg_groundedness:.3f} | "
                f"{r.avg_hallucination_risk:.3f} | {r.avg_context_precision:.3f} | "
                f"{r.avg_source_coverage:.3f} | {r.avg_latency_ms:.0f} | "
                f"{r.critical_risk_count} | {r.high_risk_count} |"
            )
        lines += [
            "",
            "*Generated by [crm-rag-eval](https://github.com/chitralabs/crm-rag-eval)*",
        ]
        import os
        os.makedirs(os.path.dirname(output_path) if os.path.dirname(output_path) else ".", exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))
        print(f"Leaderboard written to {output_path}")


