class MockRetriever:
    """
    Mock retriever for testing the benchmark pipeline.

    Simulates different retrieval quality levels without a real vector store.
    Replace with real retrievers (LangChain, LlamaIndex, ChromaDB, FAISS).
    """

    def __init__(self, quality: str = "medium", latency_ms: float = 500):
        """
        Parameters
        ----------
        quality    : "high" | "medium" | "low" — controls retrieval fidelity
        latency_ms : simulated retrieval latency
        """
        self.quality = quality
        self.latency_ms = latency_ms

    def retrieve(self, question: str, faq_item: FAQItem) -> tuple[List[str], List[str], float]:
        """
        Returns (contexts, source_ids, latency_ms).
        Replace this with real retriever logic.
        """
        time.sleep(self.latency_ms / 1000)

        if self.quality == "high":
            return (
                [faq_item.answer],
                faq_item.ground_truth_sources,
                self.latency_ms,
            )
        elif self.quality == "medium":
            # Return partial answer + one correct source
            partial = faq_item.answer[:len(faq_item.answer)//2]
            return (
                [partial, "Some generic CRM documentation excerpt."],
                faq_item.ground_truth_sources[:1],
                self.latency_ms,
            )
        else:  # low quality
            return (
                ["Generic FAQ: Please contact support for assistance."],
                [],
                self.latency_ms,
            )


def run_benchmark(
    strategies: Dict[str, Any],
    domains: Optional[List[str]] = None,
    n_samples: Optional[int] = None,
    risk_levels: Optional[List[str]] = None,
) -> BenchmarkReport:
    """
    Run a multi-strategy benchmark against the CRM FAQ dataset.

    Parameters
    ----------
    strategies  : dict mapping strategy name → retriever object with .retrieve(question, faq_item) method
    domains     : CRM domains to benchmark on (default: all)
    n_samples   : max samples to evaluate per strategy (default: all)
    risk_levels : filter FAQ items by risk level

    Returns
    -------
    BenchmarkReport with leaderboard and per-strategy metrics
    """
    faq_items = load_faq_dataset(domains=domains, risk_levels=risk_levels)
    if n_samples:
        faq_items = faq_items[:n_samples]

    evaluator = CRMRagEvaluator()
    strategy_results = []

    for strategy_name, retriever in strategies.items():
        print(f"  Benchmarking: {strategy_name} ({len(faq_items)} samples)...")
        samples = []
        for item in faq_items:
            try:
                contexts, source_ids, latency = retriever.retrieve(item.question, item)
            except Exception as e:
                contexts, source_ids, latency = [], [], 9999.0
                print(f"    Warning: retriever failed for '{item.question[:40]}': {e}")

            samples.append(RAGSample(
                question=item.question,
                generated_answer=contexts[0] if contexts else "",
                retrieved_contexts=contexts,
                ground_truth_answer=item.answer,
                ground_truth_sources=item.ground_truth_sources,
                retrieved_source_ids=source_ids,
                latency_ms=latency,
            ))

        report = evaluator.evaluate(samples)
        rs = report.results

        strategy_results.append(StrategyResult(
            name=strategy_name,
            avg_groundedness=sum(r.metrics.groundedness for r in rs) / max(len(rs), 1),
            avg_hallucination_risk=sum(r.metrics.hallucination_risk for r in rs) / max(len(rs), 1),
            avg_context_precision=sum(r.metrics.context_precision for r in rs) / max(len(rs), 1),
            avg_context_recall=sum(r.metrics.context_recall for r in rs) / max(len(rs), 1),
            avg_source_coverage=sum(r.metrics.source_coverage for r in rs) / max(len(rs), 1),
            avg_overall_score=sum(r.metrics.overall_score for r in rs) / max(len(rs), 1),
            avg_latency_ms=sum(r.metrics.latency_ms or 0 for r in rs) / max(len(rs), 1),
            critical_risk_count=sum(1 for r in rs if r.risk.risk_level.value == "critical"),
            high_risk_count=sum(1 for r in rs if r.risk.risk_level.value == "high"),
            n_samples=len(rs),
        ))

    return BenchmarkReport(
        strategy_results=strategy_results,
        dataset_size=len(faq_items),
        domains=domains or ["billing", "returns", "sla", "account_access", "product_support"],
    )


