"""HTML report formatter for crm-rag-eval evaluation results."""

from __future__ import annotations
from pathlib import Path
from typing import List, TYPE_CHECKING

if TYPE_CHECKING:
    from ..metrics.engine import MetricScores
    from ..risk.scorer import RiskAssessment


_RISK_COLORS = {
    "low":      "#d1e7dd",
    "medium":   "#fff3cd",
    "high":     "#f8d7da",
    "critical": "#842029",
}
_RISK_TEXT = {
    "low": "#0f5132", "medium": "#664d03",
    "high": "#842029", "critical": "#ffffff",
}


def render_html_report(
    metric_scores: "List[MetricScores]",
    risk_assessments: "List[RiskAssessment]",
    title: str = "CRM RAG Evaluation Report",
) -> str:
    """Render a self-contained HTML evaluation report."""

    rows = []
    for ms, ra in zip(metric_scores, risk_assessments):
        bg  = _RISK_COLORS.get(ra.risk_level.value, "#fff")
        fg  = _RISK_TEXT.get(ra.risk_level.value, "#000")
        rows.append(f"""
        <tr style="background:{bg};color:{fg}">
          <td>{ms.sample_id}</td>
          <td>{ms.answer_relevance:.3f}</td>
          <td>{ms.groundedness:.3f}</td>
          <td>{ms.context_precision:.3f}</td>
          <td>{ms.context_recall:.3f}</td>
          <td>{ms.hallucination_risk:.3f}</td>
          <td>{ms.source_coverage:.3f}</td>
          <td>{ms.latency_ms or '—'}</td>
          <td>{ms.overall_score:.3f}</td>
          <td><strong>{ra.risk_level.value.upper()}</strong></td>
          <td style="font-size:0.8em">{ra.explanation[:120]}{'...' if len(ra.explanation)>120 else ''}</td>
        </tr>""")

    risk_summary = {}
    for ra in risk_assessments:
        risk_summary[ra.risk_level.value] = risk_summary.get(ra.risk_level.value, 0) + 1

    badges = " ".join(
        f'<span style="background:{_RISK_COLORS[k]};color:{_RISK_TEXT[k]};'
        f'padding:4px 10px;border-radius:4px;margin:2px;font-weight:600">'
        f'{k.upper()}: {v}</span>'
        for k, v in risk_summary.items()
    )

    return f"""<!DOCTYPE html>
<html lang="en"><head>
<meta charset="UTF-8">
<title>{title}</title>
<style>
body{{font-family:system-ui,Arial,sans-serif;margin:2rem;color:#212529}}
h1{{color:#0d6efd}}
table{{border-collapse:collapse;width:100%;font-size:.85rem;margin-top:1rem}}
th{{background:#f8f9fa;padding:.5rem .6rem;border:1px solid #dee2e6;text-align:left}}
td{{padding:.4rem .6rem;border:1px solid #dee2e6;vertical-align:top}}
.meta{{color:#6c757d;font-size:.9rem}}
footer{{margin-top:2rem;font-size:.8rem;color:#6c757d;border-top:1px solid #dee2e6;padding-top:1rem}}
</style></head><body>
<h1>📊 {title}</h1>
<p class="meta">Generated by <strong>crm-rag-eval v1.0</strong> —
<a href="https://github.com/chitralabs/crm-rag-eval">github.com/chitralabs/crm-rag-eval</a></p>
<p class="meta">Samples evaluated: <strong>{len(metric_scores)}</strong></p>
<div style="margin:1rem 0">{badges}</div>
<table>
<thead><tr>
  <th>Sample</th><th>Relevance</th><th>Groundedness</th>
  <th>Ctx Precision</th><th>Ctx Recall</th><th>Hallucination Risk</th>
  <th>Src Coverage</th><th>Latency(ms)</th><th>Overall</th>
  <th>Business Risk</th><th>Explanation</th>
</tr></thead>
<tbody>{"".join(rows)}</tbody>
</table>
<footer>
  Disclaimer: This tool is an independent open-source project. 
  All datasets are synthetic. Not affiliated with any employer.
  See <a href="https://github.com/chitralabs/crm-rag-eval">README</a> for full disclaimer.
</footer>
</body></html>"""


def write_html_report(
    metric_scores: "List[MetricScores]",
    risk_assessments: "List[RiskAssessment]",
    output_path: str,
    title: str = "CRM RAG Evaluation Report",
) -> None:
    html = render_html_report(metric_scores, risk_assessments, title)
    Path(output_path).write_text(html, encoding="utf-8")
